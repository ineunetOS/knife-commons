package com.ineunet.knife.util.model;

import java.util.Date;

/**
 * 
 * @author Hilbert Wang
 * 
 * @since knife 1.0.0
 * 
 */
public class TimePeriod {

	private Date begin;
	private Date end;
	
	public TimePeriod () {}
	
	public TimePeriod(Date begin, Date end) {
		this.begin = begin;
		this.end = end;
	}

	public Date getBegin() {
		return begin;
	}

	public void setBegin(Date begin) {
		this.begin = begin;
	}

	public Date getEnd() {
		return end;
	}

	public void setEnd(Date end) {
		this.end = end;
	}

}
